package com.company.selftest;

public class Selftest021 {
	public static void print(int a, int b) {
        System.out.println(a + b);
    }
    public static void print(double a, double b) {
        System.out.println(a + b);
    }
    public static void main(String[] args) {
        print(3, 5);
        print(1.2, 3.4);
    }

}
